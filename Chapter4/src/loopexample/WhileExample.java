package loopexample;

public class WhileExample {

	public static void main(String[] args) {
		
		int num = 1;
		int sum = 0;
		while(num <= 10) {
			
			sum += num;
			num ++;
		}
		System.out.println("1부터 10까지의 합은" + sum + "입니다.");
		
		do { //한번 실행이 됨;
			sum += num;
			num++;
			
		}while(num < 1);
		
		System.out.println(sum);
	}

}
